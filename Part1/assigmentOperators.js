
/* Assignment operators

    += Add a number to the value of a variable
    -= Subtract a number to the value of a variable
    *= Multiply a number to the value of a variable
    /= Divide a number with the value of a variable
    %= Gets the remainder of a division and assigns it to a variable
*/

let number1 = 20;
number1+=10;
console.log(number1)

let number2= 20;
number1-=10;
console.log(number1)

let number3=20;
number3*=2;
console.log(number3)

let number4 = 20;
number4/=2;
console.log(number4)

let number5 = 20;
number5%= 6;
console.log(number5)