/* Arithmetic Operators
    = Assignment operator
    + Sum
    - Subtraction
    * Multiplication
    / Division
    % Module
    ++ Increase
    -- Decrease
*/

const result1 = 1+1;
console.log(`Sum : ${result1}`)
const result2 = 2-1;
console.log(`Rest : ${result2}`)
const result3 = 2*2;
console.log(`Multiplication : ${result3}`)
const result4 = 3/2;
console.log(`Division : ${result4}`)
const result5 = 5%3;
console.log(`Module : ${result5}`)
let result6 = 6;
result6++;
console.log(`Increase : ${result6}`)
let result7 = 6;
result7--;
console.log(`Decrease : ${result7}`)
